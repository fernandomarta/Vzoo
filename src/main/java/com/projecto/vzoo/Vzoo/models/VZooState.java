package com.projecto.vzoo.Vzoo.models;

public class VZooState {

	private Integer animalsQuantity;
	private Integer habitatsQuantity;
	private Integer employeesQuantity;
	private Integer vzooSatisfaction;

	public VZooState() {
	}

	public Integer getAnimalsQuantity() {
		return animalsQuantity;
	}

	public void setAnimalsQuantity(Integer animalsQuantity) {
		this.animalsQuantity = animalsQuantity;
	}

	public Integer getHabitatsQuantity() {
		return habitatsQuantity;
	}

	public void setHabitatsQuantity(Integer habitatsQuantity) {
		this.habitatsQuantity = habitatsQuantity;
	}

	public Integer getEmployeesQuantity() {
		return employeesQuantity;
	}

	public void setEmployeesQuantity(Integer employeesQuantity) {
		this.employeesQuantity = employeesQuantity;
	}

	public Integer getVzooSatisfaction() {
		return vzooSatisfaction;
	}

	public void setVzooSatisfaction(Integer vzooSatisfaction) {
		this.vzooSatisfaction = vzooSatisfaction;
	}
}
